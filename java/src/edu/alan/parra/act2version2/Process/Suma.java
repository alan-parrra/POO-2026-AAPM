package edu.alan.parra.act2version2.Process;

public class Suma {
    /**
     * Ejecuta la adición de dos valores numéricos para obtener su total.
     * @param op1 primer sumando de la operación
     * @param op2 segundo sumando de la operación
     * @return la magnitud resultante de combinar ambos parámetros
     */
    public static int realizarOperacion(int op1, int op2) {
        return op1 + op2;
    }
}