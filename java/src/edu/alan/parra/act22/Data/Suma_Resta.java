package edu.alan.parra.act22.Data;

public class Suma_Resta {
    public class Suma {
        public static double realizarOperacion(double a, double b) {
            return a + b;
        }
    }

    public class Resta {
        public static double realizarOperacion(double a, double b) {
            return a - b;
        }
    }
}
